function t(key, params) {
  let str = (STRINGS[currentLang] && STRINGS[currentLang][key]) || (STRINGS.uk[key]) || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return str;
}

function setLang(lang) {
  if (STRINGS[lang]) currentLang = lang;
}

function getLang() {
  return currentLang;
}

export { t, setLang, getLang, LANGS };
